# # Casting in Python
# Converting one data type into another

# casting= "changing the data type of a value to another data type is called casting"
# Type casting examples

a = int(10.5)   # float to int (decimal part will be truncated)
b = float(87)     # int to float
c = int("777")  # string to int (string must represent a valid integer)

print("a =", a)  
print("b =", b)
print("c =", c)
print("type(a) =", type(a))
print("type(b) =", type(b))  
print("type(c) =", type(c))
print("sum =", a + b + c)
print("Casting complete.")
